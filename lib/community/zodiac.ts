export function getCompatibility(sign1: string, sign2: string): number {
  const i = ZODIAC_SIGNS.indexOf(sign1 as (typeof ZODIAC_SIGNS)[number]);
  const j = ZODIAC_SIGNS.indexOf(sign2 as (typeof ZODIAC_SIGNS)[number]);
  if (i < 0 || j < 0) return 50;
  return COMPAT_MATRIX[i][j];
}

export function compatibilityLabel(score: number): string {
  if (score >= 90) return 'შესანიშნავი თავსებადობა — ძლიერი ქიმია და ურთიერთგაგება.';
  if (score >= 75) return 'კარგი თავსებადობა — ღირს ცდა და ურთიერთობა.';
  if (score >= 60) return 'საშუალო თავსებადობა — საჭიროა უფრო მეტი თავისუფალი სივრცე.';
  if (score >= 45) return 'რთული, მაგრამ საინტერესო წყვილი — განსხვავება მოგცემთ სიახლეს.';
  return 'ძალიან განსხვავებული ენერგიები — ურთიერთობას მუდმივი ძალისხმევა დასჭირდება.';
}
